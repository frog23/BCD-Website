<h2>Production</h2>

<h3>Die Aufgabe</h3>
<p>"Behind Closed Doors" war ein Filmprojekt von Studenten des Internationalen Studienganges Medieninformatik an der HTW Berlin. Im Kurs Medientechnik 2 wurde die Aufgabe gestellt, einen Kurzfilm von 5-10 Minuten Länge zu einem frei wählbarem Thema zu produzieren. Die Bewertungsschwerpunkte lagen bei Schnitt und
 Special Effects. 
</p>

<h3>Die Idee</h3>
<p>Gleich zu Beginn des Semester haben Daniel und David angefangen, eine Story für den Film zu erarbeiten. Es sollte ein Thriller werden und mit der Zeit wurden Handlung und Charaktere auch immer klarer. Bald musste allerdings eingesehen werden, dass die Menge an Idee bereits zu diesem Zeitpunkt schon zu viel für eine Kurzfilm war. So entstand die Idee eines Featurette zu einem fiktiven Spielfilm zu machen.
</p>

<h3>Die Story</h3>
<p>Uns war mit der Entscheidung ein Featurette statt einen Spielfilm zu machen auch klar, dass wir unsere Ideen zurecht stutzen mussten. Dennoch wuchs das Drehbuch mehr und mehr an. Bei 50 Seiten wurde uns abermals bewusst, dass wir den vorgegebenen Rahmen sprengen würden. Schweren Herzens mussten wir einige sehr lieb gewonnene Szenen streichen und kürzten so das Drehbuch auf 25 Seiten zurück.
</p>

<h3>Der Dreh</h3>
<p>5 Tage hatten wir für den Dreh angesetzt und genau verplant. Während des Drehens galt es dann aber verschiedenen Widrigkeiten zu trotzen. Von Textschwierigkeiten, über Stimmausfall des Hauptdarstellers bis hin zu Unwetter (Kyrill!!!) war alles dabei. Das erforderte dann auch wieder einiges an Umplanen. Dennoch gelangen uns die meisten Szenen zu unserer Zufriedenheit.
</p>

<h3>Die Nachbearbeitung</h3>
<p>Aus 4 Stunden Filmmaterial galt es nun die entscheidenden 10 Minuten herauszusuchen, mit Effekten zu versehen, richtig anzuordnen und mit Musik zu unterlegen. Insgesamt brauchten wir für nach Post-Production deutlich mehr Zeit als für das eigentliche Drehen. 20 Minuten für Abgabe war der Film dann auch tatsächlich fertig gerendert. Brauche ich eigentlich noch erwähnen, dass wir für den Film die Bestnote bekamen?
</p>
    
